package lec3;

public class Ex4 {

	public static void main(String[] args) {

			System.out.println(6/2);
			System.out.println(5.0/2); // need right amount sig figs in quest.
			System.out.println(5/2.0);
			System.out.println("------");
			System.out.println(6 % 2); // remainder = %, acts as division
			System.out.println(6 % 4); // remainder depends on the initial denominator
			System.out.println(14 % 4);
			System.out.println(-34 % 5); // remainder answer will depend on numerator being negative
			System.out.println(34 % -5); // not the denominator
			System.out.println(-34 % -5);
			System.out.println(5 % 1);
			System.out.println(1 % 5);
			
	}

}
